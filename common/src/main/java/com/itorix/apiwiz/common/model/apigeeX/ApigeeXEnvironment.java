package com.itorix.apiwiz.common.model.apigeeX;

public class ApigeeXEnvironment {
	
	private String name;
	
	private String kvmProxy;
	private String kvmProxyEndpoint;
	private String kvmProxyKey;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getKvmProxy() {
		return kvmProxy;
	}
	public void setKvmProxy(String kvmProxy) {
		this.kvmProxy = kvmProxy;
	}
	public String getKvmProxyEndpoint() {
		return kvmProxyEndpoint;
	}
	public void setKvmProxyEndpoint(String kvmProxyEndpoint) {
		this.kvmProxyEndpoint = kvmProxyEndpoint;
	}
	public String getKvmProxyKey() {
		return kvmProxyKey;
	}
	public void setKvmProxyKey(String kvmProxyKey) {
		this.kvmProxyKey = kvmProxyKey;
	}
	
	

}
